package com.jagex.entity.model;

public class VertexNormal {

	// Class33

	private int faces;

	private int x;
	private int y;
	private int z;

	public int getFaceCount() {
		return faces;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

	public int getZ() {
		return z;
	}

	public void setFaceCount(int faces) {
		this.faces = faces;
	}

	public void setX(int x) {
		this.x = x;
	}

	public void setY(int y) {
		this.y = y;
	}

	public void setZ(int z) {
		this.z = z;
	}

}